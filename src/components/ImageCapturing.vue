<template>
  <section class="section barcode-generator">
    <div class="responsive-embed">
      <video ref="scanner" class="responsive-content" autoplay></video>
    </div>
  </section>
</template>

<script lang="ts">
  import { Component, Prop, Watch, Vue } from 'vue-property-decorator';

  @Component
  export default class ImageCapturing extends Vue {
    private mounted() {
      const videoElement: HTMLVideoElement = this.$refs.scanner as HTMLVideoElement;

      navigator.mediaDevices.getUserMedia({ video: true })
        .then((mediaStream: MediaStream) => {
          videoElement.srcObject = mediaStream;
          videoElement.play();
        })
        .catch((error) => {
          console.log(error);
        });
    }
  }
</script>

<style scoped>
  .responsive-embed {
    position: relative;
    width: 100%;
    padding-bottom: 56%;
    overflow: hidden;
  }

  .responsive-content {
    position: absolute;
    top: 0;
    bottom: 0;
    left: 0;
    right: 0;
    width: 100%;
    height: 100%;
    object-fit: contain;
  }
</style>
